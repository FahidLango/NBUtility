import UIKit

extension UIFont {
    
    class func brownStdFontOfSize(_ size: CGFloat) -> UIFont! {
        return UIFont(name: "BrownStd-Regular", size: size)
    }
    
    class func brownStdBoldFontOfSize(_ size: CGFloat) -> UIFont! {
        return UIFont(name: "BrownStd-Bold", size: size)
    }
    
    class func brownStdItalicFontOfSize(_ size: CGFloat) -> UIFont! {
        return UIFont(name: "BrownStd-Italic", size: size)
    }
    
    class func brownStdLightFontOfSize(_ size: CGFloat) -> UIFont! {
        return UIFont(name: "BrownStd-Light", size: size)
    }
    
    class func brownStdLightItalicFontOfSize(_ size: CGFloat) -> UIFont! {
        return UIFont(name: "BrownStd-LightItalic", size: size)
    }
    
}
